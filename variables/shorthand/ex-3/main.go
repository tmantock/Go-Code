package main

import "fmt"

func main() {
	var a int
	var b string
	var c float64
	var d bool
	//declaring a variable with its type
	fmt.Printf("%v \n", a) //default 0
	fmt.Printf("%v \n", b) //default empty string
	fmt.Printf("%v \n", c) //default 0
	fmt.Printf("%v \n", d) //default false
	//Will print the default values
}
